			<h1>Želite li zaštiti i unaprijediti svoje zdravlje? Onda se upoznajte s vitaminima i mineralima, kao i namirnicama u kojima se kriju</h1>
			<p>Često možemo čuti kako su određene skupina vitamina i minerala važne za naše zdravlje. No vrlo rijetko nas informiraju u kojim namirnicama možemo pronaći vitamine i minerale. Vrijeme je da to ispravimo pa vam donosimo popis namirnica u kojima se kriju neki vrijedni vitamini i minerali.Vitamin A
Najveće količine vitamina A mogu se pronaći u slatkom krumpiru. Samo jedan slatki krumpir srednje veličine sadržava znatno veće količine vitamina A od preporučene doze. Možete ga pronaći i u goveđoj jetri, špinatu, ribi, mlijeku, jajima i mrkvi.<br><br>

Vitamin B6<br><br>
Ovaj dragocjeni vitamin pronaći ćete u slanutku koji ga sadržava u velikim količinama. Jedna šalica slanutka sadržava više od jednog miligrama B6 vitamina. Možete ga pronaći i u ribi, goveđoj jetri te mesu peradi.<br><br>

Vitamin B12<br><br>
I ovo je iznimno važan vitamin za ljudsko zdravlje, a najviše ga se može pronaći u kuhanim školjkama. Također, ima ga i u pastrvi, lososu, goveđoj jetri kao i u cjelovitim žitaricama.<br><br>

Vitamin C<br><br>
Većina ljudi misli da agrumi sadržavaju najviše vitamina C, ali zapravo ga slatke crvene paprike sadržavaju više od bilo koje druge hrane. Ostali dobri izvori su kivi, brokula, prokulica i dinja.<br><br>

Kalcij<br><br>
Mliječni proizvodi sadržavaju najveće količine prirodnog kalcija. Ima ga i u tamnom, lisnatom povrću kao što su kelj i kineski kupus. Također, može ga se naći i u prirodnim voćnim sokovima kao i u žitaricama.<br><br>

Vitamin D<br><br>
Masne ribe kao što su sabljarke, losos i skuša obiluju vitaminom D. Većina ljudi vitamin D uzima putem obogaćene hrane poput mlijeka, žitarica za doručak, jogurta i soka od naranče.<br><br>

Vitamin E<br><br>
Ovaj se vitamin može pronaći u pšeničnim klicama, ali ljudi ga ipak češće dobivaju iz sjemenki suncokreta ili badema jer se te namirnice češće konzumiraju.<br>

